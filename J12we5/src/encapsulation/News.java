package encapsulation;


public class News {
	private int nid;
	private String ntitle;
	private String nipcpath;
	private String nconcent;
	public int getNid() {
		return nid;
	}

	public void setNid(int nid) {
		this.nid = nid;
	}

	public String getNtitle() {
		return ntitle;
	}

	public void setNtitle(String ntitle) {
		this.ntitle = ntitle;
	}

	public String getNipcpath() {
		return nipcpath;
	}

	public void setNipcpath(String nipcpath) {
		this.nipcpath = nipcpath;
	}

	public String getNconcent() {
		return nconcent;
	}

	public void setNconcent(String nconcent) {
		this.nconcent = nconcent;
	}

	
	
}
